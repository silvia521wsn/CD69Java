public class P003 {
    public static void main(String[] args){
        //ビット演算
        System.out.println(3|5);
        System.out.println(3&5);
        System.out.println(3^5);
        System.out.println(3<<1);
        System.out.println(0x7FFFFFFF<<1);
        System.out.println(3>>1);
        System.out.println(~3);
    }
}
