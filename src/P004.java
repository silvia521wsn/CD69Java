public class P004 {
    public static void main(String[] args){
        System.out.println(3>5);//大なり
        System.out.println(3<5);//小なり
        System.out.println(3>=5);//大なりイコール or
        System.out.println(3<=5);//小なりイコール or
        System.out.println(3==5);//イコールイコール
        System.out.println(3!=5);//ノットイコール
    }
}
